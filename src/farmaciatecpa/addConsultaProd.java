    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        jTable1.setRowSelectionAllowed(true);
        String q
                = "SELECT idProducto, nombre_prod,sust_prod,desc_prod"
                + ", cant_prod, precioVenta_prod FROM Productos WHERE nombre_prod =?";

        PreparedStatement pst = null;
        ResultSet res = null;

        try {
            pst = reg.prepareStatement(q);
            pst.setString(1, jTextField1.getText());

            res = pst.executeQuery();

            while (res.next()) {

                Producto producto = new Producto(
                        res.getString("idProducto"),
                        res.getString("nombre_prod"),
                        res.getString("desc_prod"),
                        res.getString("sust_prod"),
                        res.getFloat("cant_prod"),
                        res.getFloat("precioVenta_prod")
                );
                listaProd.add(producto);
            }
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(rootPane, e);
        }

        jTextField1.setText(null);

        mostrar();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jTable1.getSelectedColumn() == 6) {
            DefaultTableModel mod = (DefaultTableModel) jTable1.getModel();
            jTable1.setRowSelectionAllowed(true);

            String indexTable = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), jTable1.getSelectedColumn()));
            int indexJTable = Integer.parseInt(indexTable);
            int removed = indexJTable;
            jTable1.setCellSelectionEnabled(true);

            idToAdd = listaProd.get(removed).getCodProducto();
            addVentas venta = new addVentas();
            venta.setOurId(idToAdd);
            setVisible(false);

        } else {
            JOptionPane.showMessageDialog(rootPane, "Para poder agregar algun "
                    + "producto presione la septima columna y luego presione el boton Agregar");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

