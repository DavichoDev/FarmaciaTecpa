    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String q = "";
        PreparedStatement pst = null;
        ResultSet res = null;
        String comboBox1 = (String) jComboBox4.getSelectedItem();
        int index;

        if (comboBox1.equals("Codigo")) {
            q = "SELECT * FROM productos WHERE idProducto = ?";
        } 
        if(comboBox1.equals("Nombre")) {
            q = "SELECT * FROM productos WHERE nombre_prod = ?";
        }

        try {
            pst = reg.prepareStatement(q);
            pst.setString(1, jTextField1.getText());
            res = pst.executeQuery();

            if (res.first()) {
                jTextField2.setText(res.getString("idProducto"));
                jTextField4.setText(res.getString("nombre_prod"));
                jTextField3.setText(res.getString("sust_prod"));
                jTextField5.setText(String.valueOf(res.getFloat("cant_prod")));
                jTextField6.setText(res.getString("desc_prod"));
                jComboBox5.setSelectedIndex(saberIndexProv(res.getString("nombre_prv")));
                if (res.getString("tipo_prod").equals("CONTROLADO")) {
                    index = 1;
                } else {
                    index = 0;
                }
                jComboBox6.setSelectedIndex(index);
                jTextField7.setText(String.valueOf(res.getFloat("precioCompra_prod")));
                jTextField8.setText(String.valueOf(res.getFloat("precioVenta_prod")));
                this.pressed = true;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Este producto no esta registrado");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        con.desconectar();
    }//GEN-LAST:event_jButton1ActionPerformed

