    public void asignarProveedores() {
        String q = "SELECT nombre_prv FROM Proveedores";
        PreparedStatement pst = null;
        ResultSet res = null;
        try {
            pst = reg.prepareStatement(q);
            res = pst.executeQuery();
            int i = 0;
            while (res.next()) {
                jComboBox5.addItem(res.getString("nombre_prv"));
                proveedores.add(res.getString("nombre_prv"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        con.desconectar();
    }

    private int saberIndexProv(String buscar) {
        for (int i = 0; i < proveedores.size(); i++) {
            if (buscar.equals(proveedores.get(i))) {
                return i;
            }
        }
        return 0;
    }

