    private void mostrar() {
        String matrix[][] = new String[listaProd.size()][7];

        for (int i = 0; i < listaProd.size(); i++) {
            matrix[i][0] = listaProd.get(i).getCodProducto();
            matrix[i][1] = listaProd.get(i).getNomProducto();
            matrix[i][2] = listaProd.get(i).getDesProducto();
            matrix[i][3] = listaProd.get(i).getSusProducto();
            matrix[i][4] = String.valueOf(listaProd.get(i).getCantProducto());
            matrix[i][5] = String.valueOf(listaProd.get(i).getPreVentaProducto());
            matrix[i][6] = String.valueOf(i);
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                matrix,
                new String[]{
                    "Codigo de producto", "Nombre", "Descripción", "Sustancia", "Cantidad", "Precio", "Localizacion"
                }
        )
        );
    }

