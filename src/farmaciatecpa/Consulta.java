    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        clearAll();
        mostrar();

        jTable1.setRowSelectionAllowed(true);

        String comboBox = (String) jComboBox1.getSelectedItem();

        String q = "";

        if (comboBox.equals("Nombre")) {
            q = "SELECT idProducto, nombre_prod,sust_prod,desc_prod"
                    + ", cant_prod, precioVenta_prod FROM Productos WHERE nombre_prod =?";
        }

        if (comboBox.equals("Sustancia")) {
            q = "SELECT idProducto, nombre_prod,sust_prod,desc_prod"
                    + ", cant_prod, precioVenta_prod FROM Productos WHERE sust_prod =?";
        }
        PreparedStatement pst = null;
        ResultSet res = null;

        try {
            pst = reg.prepareStatement(q);
            pst.setString(1, jTextField1.getText());

            res = pst.executeQuery();

            while (res.next()) {

                Producto producto = new Producto(
                        res.getString("idProducto"),
                        res.getString("nombre_prod"),
                        res.getString("desc_prod"),
                        res.getString("sust_prod"),
                        res.getFloat("cant_prod"),
                        res.getFloat("precioVenta_prod")
                );
                listaProd.add(producto);
            }
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(rootPane, e);
        }

        jTextField1.setText(null);

        mostrar();
        clearAll();
    }//GEN-LAST:event_jButton1ActionPerformed

