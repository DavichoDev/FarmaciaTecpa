    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (pressed && jTextField5.getText().length() != 0) {
            float entrada = Float.parseFloat(jTextField5.getText());
            if (entrada > 0 ) {

                if (jTextField1.getText().length() != 0) {
                    float existencia = Float.parseFloat(jTextField6.getText());
                    String query = "UPDATE Productos SET cant_prod = ? WHERE idProducto =" + jTextField4.getText();

                    try {
                        PreparedStatement stm = reg.prepareStatement(query);
                        stm.setFloat(1, (existencia + Float.parseFloat(jTextField5.getText())));
                        stm.executeUpdate();

                        JOptionPane.showMessageDialog(rootPane, "Â¡Se ha surtido el producto!");
                        jTextField1.setText(null);
                        jTextField4.setText(null);
                        jTextField2.setText(null);
                        jTextField6.setText(null);
                        jTextField5.setText(null);
                        clearAll();
                    } catch (SQLException ex) {
                        System.out.println("Error: " + ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Consulte algun producto a surtir");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Â¡No se pueden ingresar numeros negativos o iguales a 0!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Â¡Primero consulte algun producto o agregue un valor!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String q = "";

        PreparedStatement pst = null;
        ResultSet res = null;
        String comboBox = (String) jComboBox1.getSelectedItem();

        if (comboBox.equals("Codigo")) {
            q = "SELECT idProducto, nombre_prod, cant_prod FROM Productos WHERE idProducto =?";
        }

        if (comboBox.equals("Nombre")) {
            q = "SELECT idProducto, nombre_prod, cant_prod FROM Productos WHERE nombre_prod =?";
        }

        try {
            pst = reg.prepareStatement(q);
            pst.setString(1, jTextField1.getText());
            res = pst.executeQuery();

            if (res.first()) {
                jTextField4.setText(res.getString("idProducto"));
                jTextField2.setText(res.getString("nombre_prod"));
                jTextField6.setText(String.valueOf(res.getFloat("cant_prod")));
                this.pressed = true;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Este producto no esta registrado");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        con.desconectar();
    }//GEN-LAST:event_jButton1ActionPerformed

